    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        boolean bandera=false,bandera2=false;
        Entidad login = new Entidad(con);
        if(txtId.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Completa el campo Usuario");
        }else{
            bandera=true;
        }
        if(txtPass.getText().isBlank()){
            JOptionPane.showMessageDialog(null, "Completa el campo contrase√±a");
        }else{
            bandera2=true;
        }
        if(bandera && bandera2){
            int id=Integer.parseInt(txtId.getText());
            String contra=txtPass.getText();
            try {
                boolean bandera3=login.ingresar(id, contra);
                if(bandera3==true){
                    int e=login.tipoE(id);
                    //Esto fue lo que pusiste tu
                    if(e==0){//mesero
                        new MenuMesero(con).setVisible(true);
                        this.dispose();
                    }else if(e==1){//cocina
                        new MenuCocina(con).setVisible(true);
                        this.dispose();
                    }
                }else{
                    JOptionPane.showMessageDialog(null, "Error con los datos");
                    txtId.setText("");
                    txtPass.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(null, "Datos correctos");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

