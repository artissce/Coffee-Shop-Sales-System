   private void consultarBebida(int idB) throws SQLException{
        String query="SELECT * FROM bebidas WHERE id_bebida='"+idB+"'";
        PreparedStatement stmt = con.prepareStatement(query);
        
        ResultSet rs=stmt.executeQuery();
        if(rs.next()){
            tfNombreB.setText(rs.getString("nombre_bebida"));
            precioB.setText(rs.getString("precio_bebida"));
            desB.setText(rs.getString("descripcion_bebida"));
        }  else{
            JOptionPane.showMessageDialog(null, "No se encontro info bebida");
        }   
    }
