    private void btnPedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedirActionPerformed
        ExcepcionesPropias exc = new ExcepcionesPropias();
        try {
            mostrarInfo();
        } catch (SQLException ex) {
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        //verificar que ha llenado todos los espacios
        try{
            if(tfNombre.getText().equals(" ") || tfFecha.getText().equals(" ") || !bandera){
                JOptionPane.showMessageDialog(this, "Faltan datos por llenar o no hay mesa disponible");
            }else{
                try {
                    bandera2=pedido.checarIngredientesP(cont1);
                    bandera3=pedido.checarIngredientesB(cont2);

                    if(!bandera2 && bandera3){
                        JOptionPane.showMessageDialog(this, "No se puede realizar el platillo");
                        tfCantP.setText("");
                    }else if(bandera&&!bandera3){
                        JOptionPane.showMessageDialog(this, "No se puede realizar la bebida");
                        tfCantB.setText("");
                    }else{//si hay suficiente ingredientes
                        exc.cantidad(Integer.parseInt(tfCantP.getText()));
                        exc.cantidad(Integer.parseInt(tfCantB.getText()));
                        String selectedValue = (String) boxMesas.getSelectedItem();
                        int mesaSeleccionada = Integer.parseInt(selectedValue);
                        pedido.altaPedido(mesaSeleccionada, tfNombre.getText(),Integer.parseInt(tfCantP.getText()), 
                                Integer.parseInt(tfCantB.getText()),cont1, cont2, tfFecha.getText(), "En progreso");
                        if(bandera2 && bandera3){
                            pedido.reducirIngredienteB(cont2);
                            pedido.reducirIngredienteP(cont1);
                        }
                        JOptionPane.showMessageDialog(this, "Registro guardado");
                        tfNombre.setText("");
                        tfFecha.setText("");
                        tfCantP.setText("");
                        tfCantB.setText("");
                    }

                } catch (SQLException ex) {
                    System.out.println("ERROR try-catch pedido: "+ex);
                }

            }
        }catch(Exception er){
            JOptionPane.showMessageDialog(this, "Ingrese la cantidad correcta: "+er);
            tfCantP.setText("");
            tfCantB.setText("");
        }
        
    }//GEN-LAST:event_btnPedirActionPerformed

